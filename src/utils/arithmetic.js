/**
 * Because the stack ADT operates on higher-order functions, we need to be able
 * to pass it simple arithmatic functions.
 */

export const addition = (item1, item2) => item1 + item2;

export const subtraction = (item1, item2) => item1 - item2;

export const multiplication = (item1, item2) => item1 * item2;

export const division = (item1, item2) => item1 / item2;
